#!/bin/bash
#=============================================================================
# vcs_run.sh - VCS 仿真运行脚本
#
# 功能：
#   1. 执行编译生成的 simv 进行仿真
#   2. 支持 UVM 测试选择、随机种子、波形转储
#   3. 仿真结束后自动检测 PASS/FAIL
#
# 依赖环境变量：REPO_ROOT, PROJ, TC, SEED, VERBOSITY, WAVE, COV, OUTPUT_ROOT
# 基于 Synopsys VCS 2020
#=============================================================================

set -euo pipefail

#-----------------------------------------------------------------------------
# 颜色定义
#-----------------------------------------------------------------------------
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[0;33m'
NC='\033[0m'

#-----------------------------------------------------------------------------
# 路径定义
#-----------------------------------------------------------------------------
COMPILE_DIR="${OUTPUT_ROOT}/${PROJ}/compile"
SIMV="${COMPILE_DIR}/simv"

#-----------------------------------------------------------------------------
# 处理随机种子
#-----------------------------------------------------------------------------
if [ "${SEED}" = "random" ]; then
    SEED=$((RANDOM * RANDOM))
fi

#-----------------------------------------------------------------------------
# 仿真输出目录（按 测试名_种子 隔离）
#-----------------------------------------------------------------------------
SIM_DIR="${OUTPUT_ROOT}/${PROJ}/sim/${TC}_${SEED}"
SIM_LOG="${SIM_DIR}/${TC}.log"
FSDB_FILE="${SIM_DIR}/${TC}.fsdb"

mkdir -p "${SIM_DIR}"

#-----------------------------------------------------------------------------
# 检查 simv 是否存在
#-----------------------------------------------------------------------------
if [ ! -f "${SIMV}" ]; then
    echo -e "${RED}[ERROR] simv 不存在: ${SIMV}${NC}"
    echo -e "${YELLOW}[INFO]  请先运行 make compile PROJ=${PROJ}${NC}"
    exit 1
fi

#-----------------------------------------------------------------------------
# 构建仿真命令
#-----------------------------------------------------------------------------
echo -e "${GREEN}[INFO] ===== 开始仿真 =====${NC}"
echo -e "${GREEN}[INFO] 项目: ${PROJ}${NC}"
echo -e "${GREEN}[INFO] 测试: ${TC}${NC}"
echo -e "${GREEN}[INFO] 种子: ${SEED}${NC}"

SIM_CMD="${SIMV}"
SIM_CMD+=" +UVM_TESTNAME=${TC}"                  # UVM 测试类名
SIM_CMD+=" +UVM_VERBOSITY=${VERBOSITY}"          # UVM 打印级别
SIM_CMD+=" +ntb_random_seed=${SEED}"             # 随机种子
SIM_CMD+=" -l ${SIM_LOG}"                        # 仿真日志

# 波形转储
if [ "${WAVE}" = "1" ]; then
    SIM_CMD+=" +fsdbfile+${FSDB_FILE}"           # FSDB 波形文件路径
    SIM_CMD+=" +fsdb+autoflush"                  # 自动刷新 FSDB
    SIM_CMD+=" +DUMP_WAVE"                       # 通知 TB 开启波形转储
fi

# 覆盖率
if [ "${COV}" = "1" ]; then
    SIM_CMD+=" -cm line+cond+fsm+tgl+branch"     # 覆盖率类型
    SIM_CMD+=" -cm_dir ${SIM_DIR}/coverage"       # 覆盖率数据目录
    SIM_CMD+=" -cm_name ${TC}_${SEED}"            # 覆盖率实例名
fi

#-----------------------------------------------------------------------------
# 打印仿真命令
#-----------------------------------------------------------------------------
echo -e "${YELLOW}[CMD]${NC} ${SIM_CMD}"
echo ""

#-----------------------------------------------------------------------------
# 切换到仿真目录并执行
#-----------------------------------------------------------------------------
cd "${SIM_DIR}"
eval ${SIM_CMD}
SIM_EXIT=$?

#-----------------------------------------------------------------------------
# 仿真结果判定
#-----------------------------------------------------------------------------
echo ""
echo "============================================================"

if [ ! -f "${SIM_LOG}" ]; then
    echo -e "${RED}[FAIL] 仿真日志不存在，仿真可能异常退出${NC}"
    exit 1
fi

# 统计 UVM 报告中的错误和致命信息
UVM_FATAL=$(grep -c "UVM_FATAL" "${SIM_LOG}" 2>/dev/null || true)
UVM_ERROR=$(grep -c "UVM_ERROR" "${SIM_LOG}" 2>/dev/null || true)

if [ ${SIM_EXIT} -eq 0 ] && [ "${UVM_FATAL}" = "0" ] && [ "${UVM_ERROR}" = "0" ]; then
    echo -e "${GREEN}[PASS] 仿真通过${NC}"
    echo -e "${GREEN}[INFO] 测试: ${TC}  种子: ${SEED}${NC}"
else
    echo -e "${RED}[FAIL] 仿真失败${NC}"
    echo -e "${RED}[INFO] UVM_FATAL: ${UVM_FATAL}  UVM_ERROR: ${UVM_ERROR}${NC}"
    echo -e "${RED}[INFO] 测试: ${TC}  种子: ${SEED}${NC}"
fi

echo -e "[INFO] 仿真日志: ${SIM_LOG}"

if [ "${WAVE}" = "1" ] && [ -f "${FSDB_FILE}" ]; then
    echo -e "[INFO] 波形文件: ${FSDB_FILE}"
fi

echo "============================================================"

# 返回仿真退出码
exit ${SIM_EXIT}
