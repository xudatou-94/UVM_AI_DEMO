package axi_pkg;

  // -------------------------------------------------------------------------
  // 总线宽度参数
  // -------------------------------------------------------------------------
  parameter int AXI_ADDR_W = 32;
  parameter int AXI_DATA_W = 32;
  parameter int AXI_ID_W   = 8;
  parameter int AXI_STRB_W = AXI_DATA_W / 8;

  // -------------------------------------------------------------------------
  // 事务类型
  // -------------------------------------------------------------------------
  typedef enum logic {
    AXI_WRITE = 1'b1,
    AXI_READ  = 1'b0
  } axi_txn_type_e;

  // -------------------------------------------------------------------------
  // Burst 类型
  // -------------------------------------------------------------------------
  typedef enum logic [1:0] {
    AXI_BURST_FIXED = 2'b00,
    AXI_BURST_INCR  = 2'b01,
    AXI_BURST_WRAP  = 2'b10
  } axi_burst_e;

  // -------------------------------------------------------------------------
  // 响应类型
  // -------------------------------------------------------------------------
  typedef enum logic [1:0] {
    AXI_RESP_OKAY   = 2'b00,
    AXI_RESP_EXOKAY = 2'b01,
    AXI_RESP_SLVERR = 2'b10,
    AXI_RESP_DECERR = 2'b11
  } axi_resp_e;

  // -------------------------------------------------------------------------
  // 反压模式
  // -------------------------------------------------------------------------
  typedef enum logic [1:0] {
    AXI_BP_NONE   = 2'b00,  // 立即 assert READY
    AXI_BP_FIXED  = 2'b01,  // 固定延迟 N 周期后 assert
    AXI_BP_RANDOM = 2'b10   // 随机延迟 [min, max] 周期后 assert
  } axi_bp_mode_e;

  // -------------------------------------------------------------------------
  // Agent 角色
  // -------------------------------------------------------------------------
  typedef enum { AXI_MASTER, AXI_SLAVE } axi_role_e;

endpackage
